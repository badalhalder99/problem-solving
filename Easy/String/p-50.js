console.log(removeHTMLTagsCustom("<div>Hello <b>World</b></div>")); // "Hello World"
console.log(removeHTMLTagsCustom("No HTML here"));                  // "No HTML here"
console.log(removeHTMLTagsCustom(""));                              // ""

