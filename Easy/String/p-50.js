/*
===============================================================================
PROBLEM: Remove all HTML tags from a string
===============================================================================

PROBLEM STATEMENT
-----------------
Given:
- a string `str` that may contain HTML tags

Remove all HTML tags and return **only plain text**.

HTML tags are anything inside:
< tagName > ... </ tagName >

We only remove tags, NOT the text inside them.

EXAMPLE
-------
"<p>Hello</p>"                          ‚Üí "Hello"
"<div>Hello <b>World</b></div>"         ‚Üí "Hello World"
"<h1>Title</h1><p>Paragraph</p>"        ‚Üí "TitleParagraph"
"No HTML here"                          ‚Üí "No HTML here"
""                                      ‚Üí ""

KEY IDEA (BEGINNER WAY)
----------------------
To remove HTML tags:
1. Identify patterns that start with "<" and end with ">"
2. Remove everything inside those brackets
3. Keep only the visible text

CORNER CASES
------------
1. Empty string ‚Üí ""
2. Non-string input ‚Üí ""
3. String without HTML ‚Üí unchanged
4. Nested tags ‚Üí removed correctly
5. Self-closing tags ‚Üí removed

@params
-------
@param {string} str ‚Üí input string with HTML

@returns
--------
@return {string}
*/

/*
===============================================================================
SOLUTION 1: CUSTOM (Beginner Friendly, No Regex)
===============================================================================
*/

const removeHTMLTagsCustom = (str) => {
   if (typeof str !== "string") return "";

   let result = "";
   let insideTag = false;

   for (let char of str) {
      if (char === "<") {
         insideTag = true;
      } else if (char === ">") {
         insideTag = false;
      } else if (!insideTag) {
         result += char;
      }
   }

   return result;
};

/*
FUNCTION CALLS (TEST CASES)
--------------------------
*/
