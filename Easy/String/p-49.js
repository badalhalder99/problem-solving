const longestSubstringCustom = (str) => {
   // Step 1: Input validation
   if (typeof str !== "string") return 0;

   let maxLength = 0;

   // Step 2: Start from each character
   for (let i = 0; i < str.length; i++) {
      let seen = "";

      // Step 3: Build substring from i onward
      for (let j = i; j < str.length; j++) {
         // If character already exists â†’ stop
         if (seen.includes(str[j])) break;

         // Add character to seen
         seen += str[j];

         // Update maximum length
         maxLength = Math.max(maxLength, seen.length);
      }
   }

   return maxLength;
};


